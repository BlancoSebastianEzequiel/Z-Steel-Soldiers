#include "unitAction.h"
#include "dieAction.h"
#include "../game_objects/units/unit.h"
//------------------------------------------------------------------------------
// DIE ACTION CONSTRUCTOR
//------------------------------------------------------------------------------
DieAction::DieAction(Unit* unit): UnitAction(unit) {}
//------------------------------------------------------------------------------
// EXECUTE
//------------------------------------------------------------------------------
void DieAction::execute() {
    if (unit != nullptr) {
        this->unit->die();
    }
}
//------------------------------------------------------------------------------
// FINISH
//------------------------------------------------------------------------------
void DieAction::finish() {}
