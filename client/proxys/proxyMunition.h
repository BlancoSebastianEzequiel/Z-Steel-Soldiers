#ifndef PROXY_MUNITION_H
#define PROXY_MUNITION_H
//------------------------------------------------------------------------------
class ProxyNode;
//------------------------------------------------------------------------------
#include <cstddef>
#include <queue>
#include <string>
#include <utility>
//------------------------------------------------------------------------------
class ProxyMunition {
 protected :
    const size_t id;
    const size_t idShooter;
    const size_t idTarget;
    std::string currentState;
    std::string targetType;
    float speed;
    const ProxyNode* movingToDest;
    bool updated;
 public :
    //--------------------------------------------------------------------------
    ProxyMunition(
            size_t id, const size_t idShooter, const size_t idTarget);
    //--------------------------------------------------------------------------
    ~ProxyMunition();
    //--------------------------------------------------------------------------
    bool operator==(const ProxyMunition& otherMunition);
    //--------------------------------------------------------------------------
    void setMovingDest(const ProxyNode &dest);
    //--------------------------------------------------------------------------
    const ProxyNode* movingTo()const;
    //--------------------------------------------------------------------------
    void changeState(std::string newState);
    //--------------------------------------------------------------------------
    const size_t& getId()const;
    //--------------------------------------------------------------------------
    const size_t& getIdShooter()const;
    //--------------------------------------------------------------------------
    const size_t& getIdTarget();
    //--------------------------------------------------------------------------
    void setTargetType(std::string type);
    //--------------------------------------------------------------------------
    bool hasBeenUpdated();
    //--------------------------------------------------------------------------
    void setIsUpdated(bool updated);
    //--------------------------------------------------------------------------
    bool isTargetAnObject()const;
    //--------------------------------------------------------------------------
    void setSpeed(float speed);
    //--------------------------------------------------------------------------
    double getSpeed();
    //--------------------------------------------------------------------------
    bool isMoving()const;
    //--------------------------------------------------------------------------
    bool isObsolete()const;
    //--------------------------------------------------------------------------
};
//------------------------------------------------------------------------------
#endif  // PROXY_MUNITION_H
