class ProxyNode;
//------------------------------------------------------------------------------
#include <string>
#include <cstddef>
#include <vector>
//------------------------------------------------------------------------------
class ProxyUnit {
 protected :
    const ProxyNode* position;
    const size_t id;
    const std::string type;
    std::string currentState;
    size_t ownerId;
    bool positionChanged;
    float baseSpeed;
    float damageRel;
    const ProxyNode* movingToDest;
    bool updated;
    std::string lastState;
 public :
    //--------------------------------------------------------------------------
    ProxyUnit(
            const ProxyNode& position, size_t id, std::string type);
    //--------------------------------------------------------------------------
    ~ProxyUnit();
    //--------------------------------------------------------------------------
    bool operator==(const ProxyUnit& otherUnit);
    //--------------------------------------------------------------------------
    void setDamageRel(float damageRel);
    //--------------------------------------------------------------------------
    void setBaseSpeed(float baseSpeed);
    //--------------------------------------------------------------------------
    double getSpeed(const ProxyNode& aNode)const;
    //--------------------------------------------------------------------------
    const ProxyNode* getPosition()const;
    //--------------------------------------------------------------------------
    void changePosition(const ProxyNode& newPosition);
    //--------------------------------------------------------------------------
    void setMovingDest(const ProxyNode &dest);
    //--------------------------------------------------------------------------
    const ProxyNode* movingTo()const;
    //--------------------------------------------------------------------------
    void changeState(std::string  newState);
    //--------------------------------------------------------------------------
    bool lastStateIsStill();
    //--------------------------------------------------------------------------
    const size_t& getId()const;
    //--------------------------------------------------------------------------
    void setOwnerId(size_t ownerId);
    //--------------------------------------------------------------------------
    bool hasBeenUpdated();
    //--------------------------------------------------------------------------
    void setIsUpdated(bool updated);
    //--------------------------------------------------------------------------
    size_t getOwnerId();
    //-------------------------------------------------------------------------
    bool isDead()const;
    //--------------------------------------------------------------------------
    bool isMoving()const;
    //--------------------------------------------------------------------------
    bool isStill()const;
    //--------------------------------------------------------------------------
    bool isRobot()const;
    //--------------------------------------------------------------------------
    bool isVehicle()const;
    //--------------------------------------------------------------------------
    bool isGrunt()const;
    //--------------------------------------------------------------------------
    bool isTough()const;
    //--------------------------------------------------------------------------
    bool isSniper()const;
    //--------------------------------------------------------------------------
    bool isPsycho()const;
    //--------------------------------------------------------------------------
    bool isPyro()const;
    //--------------------------------------------------------------------------
    bool isLaser()const;
    //--------------------------------------------------------------------------
    bool isJeep()const;
    //--------------------------------------------------------------------------
    bool isMML()const;
    //--------------------------------------------------------------------------
    bool isLightTank()const;
    //--------------------------------------------------------------------------
    bool isMediumTank()const;
    //--------------------------------------------------------------------------
    bool isHeavyTank()const;
    //-------------------------------------------------------------------------
    bool positionHasChanged();
    //--------------------------------------------------------------------------
};
