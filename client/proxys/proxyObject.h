#ifndef PROXY_OBJECT_H
#define PROXY_OBJECT_H
//------------------------------------------------------------------------------
class ProxyNode;
//------------------------------------------------------------------------------
#include <cstddef>
#include <string>
#include <vector>
//------------------------------------------------------------------------------
class ProxyObject {
 protected :
    ProxyNode& principalPosition;
    const size_t id;
    const size_t type;
    std::string currentState;
    size_t tecnologyLevel;
    ssize_t ownerId;
    bool updated;
 public :
    //--------------------------------------------------------------------------
    ProxyObject(ProxyNode &position, size_t id, size_t type);
    //--------------------------------------------------------------------------
    ~ProxyObject();
    //--------------------------------------------------------------------------
    bool operator==(const ProxyObject& otherObject);
    //--------------------------------------------------------------------------
    bool isBroken()const;
    //--------------------------------------------------------------------------
    void setTecnologyLevel(size_t tecnologyLevel);
    //--------------------------------------------------------------------------
    const size_t& getTecnologyLevel()const;
    //--------------------------------------------------------------------------
    void changeState(std::string newState);
    //--------------------------------------------------------------------------
    bool hasBeenUpdated();
    //--------------------------------------------------------------------------
    void setIsUpdated(bool updated);
    //--------------------------------------------------------------------------
    bool isBuilding()const;
    //--------------------------------------------------------------------------
    bool isFort()const;
    //--------------------------------------------------------------------------
    bool isVehicleFactorie()const;
    //--------------------------------------------------------------------------
    bool isRobotFactorie()const;
    //--------------------------------------------------------------------------
    bool isFlag()const;
    //--------------------------------------------------------------------------
    bool isStone()const;
    //--------------------------------------------------------------------------
    bool isIceBlock()const;
    //--------------------------------------------------------------------------
    bool isBridge()const;
    //--------------------------------------------------------------------------
    bool isWoodenBridge()const;
    //--------------------------------------------------------------------------
    bool isConcreteBridge()const;
    //--------------------------------------------------------------------------
    const size_t& getId()const;
    //--------------------------------------------------------------------------
    bool hasAnOwner()const;
    //--------------------------------------------------------------------------
    void setOwnerId(ssize_t ownerId);
    //--------------------------------------------------------------------------
    const ssize_t getOwnerId()const;
    //--------------------------------------------------------------------------
    const ProxyNode& getPrincipalPosition()const;
    //--------------------------------------------------------------------------
};
//------------------------------------------------------------------------------
#endif  // PROXY_OBJECT_H
