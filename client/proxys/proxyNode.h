#ifndef PROXY_NODE_H
#define PROXY_NODE_H
//------------------------------------------------------------------------------
class ProxyTerritories;
class ProxyObject;
//------------------------------------------------------------------------------
#include <vector>
#include <memory>
//------------------------------------------------------------------------------
class ProxyNode {
 protected :
    const uint32_t x;
    const uint32_t y;
    const std::string type;
    ProxyObject* anObject;
    bool wasAnObjectAdded;
    float groundFactor;
    const ProxyTerritories* territory;
 public :
    //--------------------------------------------------------------------------
    ProxyNode(uint32_t x, uint32_t y, std::string type);
    //--------------------------------------------------------------------------
    ~ProxyNode();
    //--------------------------------------------------------------------------
    bool operator==(const ProxyNode& otherNode)const;
    //--------------------------------------------------------------------------
    bool operator!=(const ProxyNode& otherNode)const;
    //--------------------------------------------------------------------------
    void showNode()const;
    //--------------------------------------------------------------------------
    void setGroundFactor(float groundFactor);
    //--------------------------------------------------------------------------
    void addObject(ProxyObject *anObjectToAdd);
    //--------------------------------------------------------------------------
    bool hasAnObject()const;
    //--------------------------------------------------------------------------
    const ProxyObject* getGroundObject()const;
    //--------------------------------------------------------------------------
    void addTerritory(const ProxyTerritories& aTerritory);
    //--------------------------------------------------------------------------
    size_t getIdTerritory()const;
    //--------------------------------------------------------------------------
    const size_t getOwnerId()const;
    //--------------------------------------------------------------------------
    const float& getGroundFactor()const;
    //--------------------------------------------------------------------------
    bool isLava()const;
    //--------------------------------------------------------------------------
    bool isDust()const;
    //--------------------------------------------------------------------------
    bool isGrass()const;
    //--------------------------------------------------------------------------
    bool isSnow()const;
    //--------------------------------------------------------------------------
    bool isWater()const;
    //--------------------------------------------------------------------------
    bool isSwamp()const;
    //--------------------------------------------------------------------------
    bool isRoad()const;
    //--------------------------------------------------------------------------
    bool isAsphaltRoad()const;
    //--------------------------------------------------------------------------
    const uint32_t getX()const;
    //--------------------------------------------------------------------------
    const uint32_t getY()const;
    //--------------------------------------------------------------------------
};
//------------------------------------------------------------------------------
#endif  // PROXY_NODE_H
