#include <iostream>
#include "client_Button.h"
//------------------------------------------------------------------------------
// CLIENT BUTTON CONSTRUCTOR
//------------------------------------------------------------------------------
ClientButton::ClientButton(
        unsigned int id, unsigned int pos_x, unsigned int pos_y):
        GameObject(id, pos_x, pos_y), visible(false) {}
//------------------------------------------------------------------------------
// CLIENT BUTTON DESTRUCTOR
//------------------------------------------------------------------------------
ClientButton::~ClientButton() {
    for (unsigned int i = 0; i < frames.size(); i++) {
        frames[i].unload();
    }
}
//------------------------------------------------------------------------------
// DRAW
//------------------------------------------------------------------------------
void ClientButton::draw(SDL_Surface* screen, ClientCamera& camera) {
    if (visible) {
        GameObject::draw(screen, camera);
    }
}
//------------------------------------------------------------------------------
// SHOW
//------------------------------------------------------------------------------
void ClientButton::show() {
    visible = true;
}
//------------------------------------------------------------------------------
// HIDE
//------------------------------------------------------------------------------
void ClientButton::hide() {
    visible = false;
}
//------------------------------------------------------------------------------
