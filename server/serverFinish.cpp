#include "serverFinish.h"
#include "server.h"
//------------------------------------------------------------------------------
// FINISH CONSTRUCTOR
//------------------------------------------------------------------------------
Finish::Finish(Server& aServer): aServer(aServer) {}
//------------------------------------------------------------------------------
// FINISH DESTRUCTOR
//------------------------------------------------------------------------------
Finish::~Finish() {}
//------------------------------------------------------------------------------
// RUN
//------------------------------------------------------------------------------
void Finish::run() {
    while (fgetc(stdin) != FINISH) {
        usleep(WAIT_TIME);
    }
    aServer.stop();
}
