#include "nodePath.h"
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
// SERVER NODE PATH CONSTRUCTOR
//------------------------------------------------------------------------------
NodePath::NodePath(
        const Node& aNode, State& state, float cumulativeSpeed):
        node(aNode), cumulativeSpeed(cumulativeSpeed) {
    this->state = &state;
}
//------------------------------------------------------------------------------
// SERVER NODE PATH DESTRUCTOR
//------------------------------------------------------------------------------
NodePath::~NodePath() {}
//------------------------------------------------------------------------------
// GET STATE
//------------------------------------------------------------------------------
State* NodePath::getState() const {
    return state;
}
//------------------------------------------------------------------------------
// GET NODE
//------------------------------------------------------------------------------
const Node& NodePath::getNode() const {
    return node;
}
//------------------------------------------------------------------------------
// GET CUMULATIVE WAITING TIME
//------------------------------------------------------------------------------
float NodePath::getCumulativeWaitingTime() const {
    return cumulativeSpeed;
}
//------------------------------------------------------------------------------
// CHANGE STATE
//------------------------------------------------------------------------------
void NodePath::changeState(State& newState) {
    state = &newState;
}
