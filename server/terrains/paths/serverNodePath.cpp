#include "serverNodePath.h"
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
// SERVER NODE PATH CONSTRUCTOR
//------------------------------------------------------------------------------
ServerNodePath::ServerNodePath(
        const ServerNode& aNode, ServerState& state, float cumulativeSpeed):
        node(aNode), cumulativeSpeed(cumulativeSpeed) {
    this->state = &state;
}
//------------------------------------------------------------------------------
// SERVER NODE PATH DESTRUCTOR
//------------------------------------------------------------------------------
ServerNodePath::~ServerNodePath() {}
//------------------------------------------------------------------------------
// GET STATE
//------------------------------------------------------------------------------
ServerState* ServerNodePath::getState() const {
    return state;
}
//------------------------------------------------------------------------------
// GET NODE
//------------------------------------------------------------------------------
const ServerNode& ServerNodePath::getNode() const {
    return node;
}
//------------------------------------------------------------------------------
// GET CUMULATIVE WAITING TIME
//------------------------------------------------------------------------------
float ServerNodePath::getCumulativeWaitingTime() const {
    return cumulativeSpeed;
}
//------------------------------------------------------------------------------
// CHANGE STATE
//------------------------------------------------------------------------------
void ServerNodePath::changeState(ServerState& newState) {
    state = &newState;
}
