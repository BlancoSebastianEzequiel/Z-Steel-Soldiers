#include "../../terrains/node.h"
#include "robot.h"
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
// ROBOT CONSTRUCTOR
//------------------------------------------------------------------------------
Robot::Robot(const Node& initialPosition, size_t id):
        Unit::Unit(initialPosition, id) {}
//------------------------------------------------------------------------------
// ROBOT DESTRUCTOR
//------------------------------------------------------------------------------
Robot::~Robot() {}
//------------------------------------------------------------------------------
// GET COST
//------------------------------------------------------------------------------
double Robot::getCost(const Node &aNode) {
    return aNode.getRobotCost(*this);
}
//------------------------------------------------------------------------------
// GET SPEED
//------------------------------------------------------------------------------
double Robot::getSpeed(const Node &aNode) const {
    float finalVelocity;
    finalVelocity = 4 * aNode.getGroundFactor();
    if (finalVelocity > 1) {
        return finalVelocity;
    }
    return 1;
}
//------------------------------------------------------------------------------
// CAN PASS THROUGH
//------------------------------------------------------------------------------
bool Robot::canPassThrough(const Node &aNode) const {
    return aNode.robotPassThrough();
}
//------------------------------------------------------------------------------
// IS ROBOT
//------------------------------------------------------------------------------
bool Robot::isRobot() const {
    return true;
}
//------------------------------------------------------------------------------
// IS VEHICLE
//------------------------------------------------------------------------------
bool Robot::isVehicle() const {
    return false;
}
