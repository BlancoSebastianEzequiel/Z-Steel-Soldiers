#include "team.h"
#include "../players/player.h"
//------------------------------------------------------------------------------
// PLAYER CONSTRUCTOR
//------------------------------------------------------------------------------
Team::Team(const size_t id): id(id) {}
//------------------------------------------------------------------------------
// PLAYER DESTRUCTOR
//------------------------------------------------------------------------------
Team::~Team() {}
//------------------------------------------------------------------------------
// GET ID
//------------------------------------------------------------------------------
const size_t& Team::getId() const {
    return id;
}
//------------------------------------------------------------------------------
// ADD PLAYER
//------------------------------------------------------------------------------
void Team::addPlayer(Player &aPlayer) {
    playersTeam(aPlayer.getId(), &aPlayer);
    aPlayer.addTeam(*this);
}
//------------------------------------------------------------------------------
// ADD PLAYER
//------------------------------------------------------------------------------
void Team::erasePlayer(size_t idPlayer) {
    playersTeam.erase(idPlayer);
}
//------------------------------------------------------------------------------
// BELONGS
//------------------------------------------------------------------------------
bool Team::belongs(const Player& aPlayer) const {
    return playersTeam.keyExist(aPlayer.getId());
}
//------------------------------------------------------------------------------
// HAS LOST
//------------------------------------------------------------------------------
bool Team::hasLost() {
    for (Player* aPlayer : playersTeam.getList()) {
        if (!aPlayer->hasLost()) return false;
    }
    return true;
}
