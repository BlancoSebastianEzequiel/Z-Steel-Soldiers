#include <tgmath.h>
#include "task.h"
//------------------------------------------------------------------------------
// SERVER TASK CONSTRUCTOR
//------------------------------------------------------------------------------
Task::Task(Game& aGame): aGame(aGame) {
    time(&timer);
}
//------------------------------------------------------------------------------
// SERVER TASK DESTRUCTOR
//------------------------------------------------------------------------------
Task::~Task() {}
//------------------------------------------------------------------------------
// CHANGE WAITING TIME
//------------------------------------------------------------------------------
void Task::changeWaitingTime(double newWaitingTime) {
    time(&timer);
    waitingTime = newWaitingTime;
}
//------------------------------------------------------------------------------
// EXECUTABLE
//------------------------------------------------------------------------------
bool Task::executable() {
    double timeElapsed =  difftime(time(nullptr), timer);
    return timeElapsed >= (waitingTime);
}
