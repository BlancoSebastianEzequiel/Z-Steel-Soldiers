#ifndef  EXCEPTION_H
#define  EXCEPTION_H
//------------------------------------------------------------------------------
#include <typeinfo>
//------------------------------------------------------------------------------
#define BUF_LEN 256
//------------------------------------------------------------------------------
class Exception : public std::exception {
 private :
    char msg_error[BUF_LEN];
 public :
    //--------------------------------------------------------------------------
    explicit Exception(const char* fmt, ...) noexcept;
    //--------------------------------------------------------------------------
    virtual const char* what() const noexcept;
    //--------------------------------------------------------------------------
    virtual ~Exception() noexcept {}
    //--------------------------------------------------------------------------
};
//------------------------------------------------------------------------------
#endif  // EXCEPTION_H
